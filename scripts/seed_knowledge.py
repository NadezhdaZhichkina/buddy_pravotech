def main():
    import argparse
    from sqlalchemy import delete

    parser = argparse.ArgumentParser(description="База знаний Buddy")
    parser.add_argument("--clear", action="store_true", help="Очистить базу перед заполнением")
    args = parser.parse_args()

    settings = get_settings()
    engine = create_engine(settings.database_url)
    Session = sessionmaker(bind=engine)
    session = Session()

    if args.clear:
        n = session.query(KnowledgeItem).count()
        session.execute(delete(KnowledgeItem))
        session.commit()
        print(f"Удалено записей: {n}")

    added = 0
    updated = 0
    for item in SEED_ITEMS:
        existing = session.query(KnowledgeItem).filter(
            KnowledgeItem.question == item["question"]
        ).first()
        if existing:
            if existing.answer != item["answer"] or existing.tags != item["tags"]:
                existing.answer = item["answer"]
                existing.tags = item["tags"]
                updated += 1
                print(f"  ~ Обновлено: {item['question'][:50]}...")
            else:
                print(f"  Пропуск (уже есть): {item['question'][:50]}...")
            continue
        ki = KnowledgeItem(
            question=item["question"],
            answer=item["answer"],
            tags=item["tags"],
        )
        session.add(ki)
        added += 1
        print(f"  + {item['question'][:60]}...")

    session.commit()
    session.close()
    print(f"\nГотово. Добавлено: {added}, обновлено: {updated}")


if __name__ == "__main__":
    main()
